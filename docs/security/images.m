### Introdução
Sobre os aspectos de upload e tratamento de arquivos dentro do BLiP, foram liberadas recentemente três novidades, relacionadas a:

* verificação de vírus;

* validação de *media types*;

* tamanho máximo do arquivo.


### Verificação de vírus
Ao enviar um arquivo, o usuário tem o nome conferido e, usando o ClamAv, é realizada uma verificação sobre a presença de algum vírus, trojan ou malware.

### Validação de *media types*
Os arquivos suportados pelo BLiP estão listados abaixo:

* audio/aac

* audio/midi

* audio/mp3

* audio/mp4

* audio/mpeg

* audio/wav

* audio/ogg

* audio/wma

* audio/webm

* audio/opus

* audio/x-aac

* image/gif

* image/jpeg

* image/jpg

* image/jfif

* image/png

* image/svg+xml

* image/tiff

* image/vnd.dwg

* image/webp

* video/3gpp

* video/avi

* video/mpeg

* video/mpg

* video/mp4

* video/mov

* video/m4v

* video/wmv

* video/webm

* video/ogg

* application/pdf

* application/vnd.ms-excel

* application/vnd.openxmlformats-officedocument.spreadsheetml.sheet

* application/vnd.openxmlformats-officedocument.spreadsheetml.template

